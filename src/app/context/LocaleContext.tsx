interface LocaleContextType {
  locale: LocaleCode;
  t: (key: string) => string;
  setLocale: (l: LocaleCode) => void;
}

const LocaleContext = createContext<LocaleContextType | undefined>(undefined);

export function LocaleProvider({ children }: { children: ReactNode }) {
  const [locale, setLocaleState] = useState<LocaleCode>('en');

  // Load locale from localStorage on mount
  useEffect(() => {
    const savedLocale = localStorage.getItem('preferredLocale') as LocaleCode | null;
    if (savedLocale && ['en', 'lg', 'rk', 'ac', 'teo', 'lgg', 'cgg', 'sw', 'ny', 'nd'].includes(savedLocale)) {
      setLocaleState(savedLocale);
    }
  }, []);

  const setLocale = (l: LocaleCode) => {
    setLocaleState(l);
    localStorage.setItem('preferredLocale', l);
  };

  const t = (key: string) => {
    const messages = {
      ...TRANSLATIONS.en,
      ...LOCALIZED_ONBOARDING.en,
      ...(TRANSLATIONS[locale] || {}),
      ...(LOCALIZED_ONBOARDING[locale] || {}),
    };
    return messages[key] ?? key;
  };

  return (
    <LocaleContext.Provider value={{ locale, t, setLocale }}>
      {children}
    </LocaleContext.Provider>
  );
}

export function useLocale() {
  const ctx = useContext(LocaleContext);
  if (!ctx) throw new Error('useLocale must be used within LocaleProvider');
  return ctx;
}

export default LocaleProvider;
